# Importa a função 'choice' do módulo 'random'
from random import choice

# Define uma lista de números
lista = [8, 12, 54, 23, 43, 1, 90, 87, 105, 77]

# Usa a função 'choice' para selecionar aleatoriamente um número da lista e imprime o número escolhido
print(f"Número escolhido: {choice(lista)}")


